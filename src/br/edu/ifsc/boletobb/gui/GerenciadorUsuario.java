    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try{
            String nome =(String) tabela.getModel().getValueAt(tabela.getSelectedRow(), 0); //pegar linha selecionada da coluna 0 para sempre pegar o nome
            String situacao = (String)tabela.getModel().getValueAt(tabela.getSelectedRow(), 1);//pegar linha selecionada da coluna 1 para sempre pegar a situaçao
            String sql;
            if(situacao.equals("Ativo")){
                sql = "update usuario set usua_situacao = " + 0 + " where usua_login like '"+nome+"';";
            } else {
                sql = "update usuario set usua_situacao = " + 1 + " where usua_login like '"+nome+"';";
            }
            banco.bdAtualizar(sql);
            limparTabela();
            iniciarTabela();
        } catch (java.lang.ArrayIndexOutOfBoundsException ex){
            JOptionPane.showMessageDialog(null, "Selecione uma linha!");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        if(jTextField1.getText().isEmpty()){
            limparTabela();
            iniciarTabela();
        } else {
            limparTabela();
            banco = new BancoDeDados();
            String[] print = new String[2];
            String sql = "select * from usuario where usua_login like '%"+jTextField1.getText()+"%' order by usua_login;";
            ResultSet result = banco.bdConsultar(sql);

            try {
                while (result.next()){
                    print[0] = result.getString("usua_login");
                    print[1] = result.getString("usua_situacao");
                    ((DefaultTableModel)tabela.getModel()).addRow(print);
                }
            } catch (SQLException ex) {
                Logger.getLogger(Inicio.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jTextField1KeyReleased

